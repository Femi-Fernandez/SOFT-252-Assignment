    private void CombPatientIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CombPatientIDActionPerformed
        // TODO add your handling code here:
        String patID = CombPatientID.getSelectedItem().toString();
        TxtPatientInfo.setText("");
        for (int i = 0; i < SystemDatabase.userArray.size(); i++) {
            if (SystemDatabase.userArray.get(i).getUserID().equals(patID) ) {
                TxtPatientInfo.setText("Firstame: " +SystemDatabase.userArray.get(i).getUserFirstname() + "\n"+
                                      "Surname: " + SystemDatabase.userArray.get(i).getUserSurname() + "\n"+
                                      "Address: " + SystemDatabase.userArray.get(i).getUserAddress() + "\n"+
                                      "Age: " +SystemDatabase.userArray.get(i).getUserAge() + "\n"+ 
                                      "Gender: " + SystemDatabase.userArray.get(i).getUserGender());
                break;
            }
        }
    }//GEN-LAST:event_CombPatientIDActionPerformed

    private void CombMedNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CombMedNameActionPerformed
        // TODO add your handling code here:
        String medName = CombMedName.getSelectedItem().toString();
        TxtMedInfo.setText("");
        for (int i = 0; i < SystemDatabase.medicineArray.size(); i++) {
            if (SystemDatabase.medicineArray.get(i).getMedicineName().equals(medName)) {
                TxtMedInfo.setText("Medicine: " +SystemDatabase.medicineArray.get(i).getMedicineName() +"\n"+
                                   "Current stock: " + SystemDatabase.medicineArray.get(i).getStock());
            }
        }
    }//GEN-LAST:event_CombMedNameActionPerformed

