    public void SetPatCombValues(){
        ArrayList<String> a = new ArrayList<String>();
            for (int i = 0; i < SystemDatabase.userArray.size(); i++) {
                if (SystemDatabase.userArray.get(i).getUserID().charAt(0) == 'P') {
                    a.add(SystemDatabase.userArray.get(i).getUserID());
                }         
        }
        for (String value : a) 
        {
            CombPatientID.addItem(value); 
        }
    }  
    public void SetMedCombValues(){
        ArrayList<String> a = new ArrayList<String>();
            for (int i = 0; i < SystemDatabase.medicineArray.size(); i++) {
                    a.add(SystemDatabase.medicineArray.get(i).getMedicineName());
        }
        for (String value : a) 
        {
            CombPatientID.addItem(value); 
        }
    }   

